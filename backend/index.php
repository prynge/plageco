                 <P style="font-family: cursive;">Historique</P>
                 <P>Le concept derrière 1000 & Une Piles est né en Angleterre. L’idée de départ est de présenter l’offre la plus large possible en matière de produits d’énergie autonome et de s’appuyer sur la connaissance et le savoir-faire de techniciens. Victime d’une forte croissance, le groupe lyonnais, VDI Group avec à sa tête <em>Thierry BOUVAT</em> (Directeur Général du groupe depuis Août 1998), rachète l’enseigne en 1999 et fait évoluer le concept. Le nombre de boutiques évolue et en 2006, ce sont quatorze (14) magasins en France (sept (7) en nom propres et sept (7) en franchise). Le réseau franchisé s’étoffant, 1000 & Une Piles choisit de faire également évoluer son enseigne et son logo pour mieux répondre aux besoins des consommateurs. Dorénavant, les magasins porteront le nom de 1001 Piles Batteries, enseigne qui se décline aussi bien en France qu’à l’étranger. L’Espagne est le premier pays à rejoindre les franchisés hexagonaux avec l’ouverture de trois (3) nouveaux magasins, suivi du Togo qui ouvre une franchise africaine en 2013. Aujourd’hui, le réseau 1001 Piles Batteries se compose de vingt (20) magasins en propre et soixante-et-un (61) en franchise.</P>
